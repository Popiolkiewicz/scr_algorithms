package pl.scr.project.model;

public class ChartElement {

	private int start;
	private int end;

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	@Override
	public String toString() {
		return "ChartElement [start=" + start + ", end=" + end + "]";
	}

}